#!/usr/bin/env python3
"""Entry point for the LiveKit Operations Console backend."""

import uvicorn


def main():
    """Run the FastAPI application."""
    uvicorn.run(
        "app.main:app",
        host="0.0.0.0",
        port=8000,
        reload=True,
        log_level="info",
    )


if __name__ == "__main__":
    main()
